void	solucao(void)
{
	int i;

	i = 0;
	while (i <=3)
//Quando for 4: 1234 e quando 1: 4
	{
		if (head[i] == 4)
		{
			l1[i] =  '1';
			l2[i] =  '2';
		    l3[i] =  '3';
		    l4[i] =  '4';
		}

		if (head[i] == 1)
		{
			l1[i] = '4';
		}

		if (bottom[i] == 4)
		{
			l1[i] =  '4';
			l2[i] =  '3';
		    l3[i] =  '2';
		    l4[i] =  '1';
		}

		if (bottom[i] == 1)
		{
			l4[i] = '4';
		}

		if (left[i] == 4)
		{
			if (i==0)
			{
				l1[0] = '1';
				l1[1] = '2';
				l1[2] = '3';
				l1[3] = '4';
			}
			if (i==1)
			{
				l2[0] = '1';
				l2[1] = '2';
				l2[2] = '3';
				l2[3] = '4';
			}
			if (i==2)
			{
				l3[0] = '1';
				l3[1] = '2';
				l3[2] = '3';
				l3[3] = '4';
			}
			if (i==3)
			{
				l4[0] = '1';
				l4[1] = '2';
				l4[2] = '3';
				l4[3] = '4';
			}
		}
		if (left[i] == 1)
		{
			if(i == 0)
				l1[0] = '4';
			if(i == 1)
				l2[0] = '4';
			if(i == 2)
				l3[0] = '4';
			if(i == 3)
				l4[0] = '4';
		}
		if (right[i] == 4)
		{
			if(i==0)
			{
				l1[0] = '4';
				l1[1] = '3';
				l1[2] = '2';
				l1[3] = '1';
			}
			if(i==1)
			{
				l2[0] = '4';
				l2[1] = '3';
				l2[2] = '2';
				l2[3] = '1';
			}
			if(i==2)
			{
				l3[0] = '4';
				l3[1] = '3';
				l3[2] = '2';
				l3[3] = '1';
			}
			if(i==3)
			{
				l4[0] = '4';
				l4[1] = '3';
				l4[2] = '2';
				l4[3] = '1';
			}
		}

		if (right[i] == 1)
		{
			if(i == 0)
				l1[3] = '4';
			if(i == 1)
				l2[3] = '4';
			if(i == 2)
				l3[3] = '4';
			if(i == 3)
				l4[3] = '4';
		}

// Quando for 2 de um lado e 1 do outro, canto 2 recebe 3
	if(left[i] == 2 && right[i] == 1)
	{
		if(i == 0)
			l1[0] = '3';
		if(i == 1)
			l2[0] = '3';
		if(i == 2)
			l3[0] = '3';
		if(i ==3)
			l4[0] = '3';
	}

	if(head[i] == 2 && bottom[i] == 1)
	{
		l1[i] = '3';
	}

	if(left[i] == 1 && right[i] == 2)
	{
		if(i == 0)
			l1[3] = '3';
		if(i == 1)
			l2[3] = '3';
		if(i == 2)
			l3[3] = '3';
		if(i ==3)
			l4[3] = '3';
	}

	if(head[i] == 1 && bottom[i] == 2)
	{
		l4[i] = '3';
	}
// Quando 3 e 2 forem opostos, o numero 4 fica a duas posiÃ§oes do 2 e tres do 3
	if(left[i] == 3 && right[i] == 2)
	{
		if(i == 0)
			l1[2] = '4';
		if(i == 1)
			l2[2] = '4';
		if(i == 2)
			l3[2] = '4';
		if(i ==3)
			l4[2] = '4';
	}

	if(head[i] == 3 && bottom[i] == 2)
	{
		l3[i] = '4';
	}

	if(left[i] == 2 && right[i] == 3)
	{
		if(i == 0)
			l1[1] = '4';
		if(i == 1)
			l2[1] = '4';
		if(i == 2)
			l3[1] = '4';
		if(i ==3)
			l4[1] = '4';
	}

	if(head[i] == 2 && bottom[i] == 3)
	{
		l2[i] = '4';
	}
	i++;
	}
	completar4(void);
	completar3(void);
}
